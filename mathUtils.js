//Addition function
export function addition(addX, addY) {
    return addX + addY;
}
//Subtraction function
export function subtraction(subX, subY) {
    return subX - subY;
}
//Multiply function
export function multiply(multX, multY) {
    return multX * multY;
}
//Division function
export function division(divideX, divideY) {
    return divideX / divideY;
}
//C to F function
export function convertDegree(celsius) {
    return celsius * (9 / 5) + 32;
} 

